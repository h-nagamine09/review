# for inによるループ処理

# for カウンタ変数 in 繰り返す範囲
#     繰り返し処理
# end

for i in 0..9
    puts "hello world" #繰り返し処理
end

# for in によるループ処理
#演習課題「「ハロー、paizaラーニング」を5回表示する」
for i in 0..4
  puts "ハロー、paizaラーニング"
end

# for in によるループ処理
# 演習課題「数値を0から15まで表示する
for i in 0..15
  puts i
end

# for in によるループ処理
# 演習課題「1月から12月まで表示する」
for i in 1..12
  puts "#{i}月"
end
