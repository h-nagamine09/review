#------------------------------------------------------------------------------------------
# 指定の文字をハッシュにする
# 演習課題「指定の文字をハッシュにしてみよう」
#  skillsというハッシュに、下記の要素をこのキーと値を組にして保存して、pメソッドで出力をしてください
#
# 職業 : "戦士"
# 体力 : 100
# 魔法力 : 200
# ゴールド : 380

skills = {"職業" => "戦士","体力" => 100,"魔法力" => 200,"ゴールド" => 380}

p skills
#------------------------------------------------------------------------------------------
# ハッシュの特定の値を出力してみよう
# 右のコードには、ハッシュが定義されています。
# このハッシュから、「職業」というキーに対応する値をputsで出力してください。
skills = {"職業" => "戦士", "体力" => 100, "魔法力" => 200, "ゴールド" => 380}
# この下で、ハッシュから出力してみよう
puts skills["職業"] #ハッシュを指定する場合、大括弧を使用
#------------------------------------------------------------------------------------------
# ハッシュの基本操作

# ハッシュのおさらい
enemies = {"ザコ" => "スライム", "中ボス" => "ドラゴン", "ラスボス" => "魔王"}
p enemies
puts enemies["ザコ"]

puts enemies.length #ハッシュの個数を調べる

enemies["ザコ２"] = "メタルモンスター" #ハッシュを追加
p enemies
puts enemies.length

enemies["中ボス"] = "レッドドラゴン" #ハッシュの中身を変更
p enemies
puts enemies.length

enemies.delete("ザコ") #ハッシュを削除
p enemies
puts enemies.length
#------------------------------------------------------------------------------------------
# ハッシュに要素を追加する

skills = {"職業" => "戦士", "体力" => 100, "魔法力" => 200, "ゴールド" => 380}
# この下で、ハッシュにデータを追加してみよう

skills["属性"] = "炎"

p skills
#------------------------------------------------------------------------------------------
# ハッシュに要素を更新する

skills = {"職業" => "戦士", "体力" => 100, "魔法力" => 200, "ゴールド" => 380}
p skills
# この下で、ハッシュを更新してみよう
skills["職業"] = "魔法使い"
p skills
#------------------------------------------------------------------------------------------
# ハッシュの要素を削除する

skills = {"職業" => "戦士", "体力" => 100, "魔法力" => 200, "ゴールド" => 380}
p skills
# この下で、ハッシュを削除してみよう
skills.delete("体力")
p skills
#------------------------------------------------------------------------------------------
# ハッシュをループで処理する
enemies = {"ザコ" => "スライム", "中ボス" => "ドラゴン", "ラスボス" => "魔王"}
p enemies
puts enemies["ザコ"]

enemies.each_value do |enemy| #each_value値のみ
    puts "#{enemy}が現れた！"
end

enemies.each do |rank,enemy| #each キーと値を取り出す
    puts "#{rank}の#{enemy}が現れた！"
end
#------------------------------------------------------------------------------------------
# ループでハッシュのキーと値を出力しよう

skills = {"職業" => "戦士", "体力" => 100, "魔法力" => 200, "ゴールド" => 380}
# この下で、ハッシュの値をループで出力してみよう
skills.each_value do |skill|
    puts skill
end
# 期待する出力値
# 戦士
# 100
# 200
# 380
#------------------------------------------------------------------------------------------
